    private void jTable12MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable12MouseClicked
        // TODO add your handling code here:
        int row = jTable12.getSelectedRow();
        if(row >=0){
            txtUsername.setText(jTable12.getValueAt(row, 0).toString());
            txtPassword.setText(jTable12.getValueAt(row, 1).toString());
            txtName.setText(jTable12.getValueAt(row, 2).toString());
            String power =jTable12.getValueAt(row, 3).toString();
            if(power.equals("1")) CBpower.setSelectedItem("Nhân viên");
            else if(power.equals("2"))CBpower.setSelectedItem("Admin");
            else CBpower.setSelectedItem("Owner");
            txtPhone.setText(jTable12.getValueAt(row, 4).toString());
            txtAddress.setText(jTable12.getValueAt(row, 5).toString());
        }
        
    }//GEN-LAST:event_jTable12MouseClicked

