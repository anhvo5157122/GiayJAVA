    public void showTable(){
        model.setRowCount(0);
        for(supplier s : listSupplier){
            model.addRow(new Object[]{
                s.getSupplierId(), s.getSupplierName(), s.getPhone()
            });
        }
    }
    public void showTableSearch(ArrayList<supplier> listSupplierSearch){
        model = (DefaultTableModel) jTable6.getModel();
        model.setRowCount(0);
        for(supplier s : listSupplierSearch){
            model.addRow(new Object[]{
                s.getSupplierId(), s.getSupplierName(), s.getPhone()
            });
        }
    }
   
    
