    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String txtSearch = this.txtSearch.getText();
        if(!txtSearch.equals("")){
            
           ArrayList<supplier> listId = supplierBLL.searchById(txtSearch);
           ArrayList<supplier> listName = supplierBLL.searchByName(txtSearch);
           ArrayList<supplier> listPhone = supplierBLL.searchByPhone(txtSearch);
        if(!listId.isEmpty()){
            showTableSearch(listId);
        }
        else if(!listName.isEmpty()){
            showTableSearch(listName);
        }
        else if(!listPhone.isEmpty()){
            showTableSearch(listPhone);
        }
        else JOptionPane.showMessageDialog(null, "Không tìm thấy.");
        }
        else{
            JOptionPane.showMessageDialog(null, "Vui long nhập dữ liệu cần tìm kiếm.");
            load();
        }

    }//GEN-LAST:event_btnSearchActionPerformed
    public void clear(){
        txtSupplierName.setText("");
        txtSupplierPhone.setText("");
        txtSupplierId.setText("");
    }
    
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String supplierName = txtSupplierName.getText();
        String supplierPhone = txtSupplierPhone.getText();
        supplier s = new supplier(supplierName, supplierPhone);
        if(!supplierName.equals("")&&!supplierPhone.equals("")){
            if(supplierBLL.addSupplier(s)>0){
            JOptionPane.showMessageDialog(null, "Thêm thành công.");
            load();
            clear();
        }
        else{
            JOptionPane.showMessageDialog(null, "Thêm thất bại.");
        }
        }else{
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin nhà cung cấp cần thêm.");
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void jTable6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable6MouseClicked
        // TODO add your handling code here:
        int row = jTable6.getSelectedRow();
        if(row >=0){
            txtSupplierId.setText(jTable6.getValueAt(row, 0).toString());
            txtSupplierName.setText(jTable6.getValueAt(row, 1).toString());
            txtSupplierPhone.setText(jTable6.getValueAt(row, 2).toString());
        }
        
        
    }//GEN-LAST:event_jTable6MouseClicked

    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        if(!txtSupplierId.getText().equals("")){
            if(supplierBLL.getSupplierById(Integer.parseInt(txtSupplierId.getText()))!=null){
                if(supplierBLL.deleteSupplier(Integer.parseInt(txtSupplierId.getText()))>0){
                    JOptionPane.showMessageDialog(null, "Xóa thành công.");
                    load();
                    clear();
                }else JOptionPane.showMessageDialog(null, "Xóa thất bại.");
            
        }else{
            JOptionPane.showMessageDialog(null, "Không tìm thấy nhà cung cấp cần xóa.");
        }
        }else{
            JOptionPane.showMessageDialog(null, "Vui lòng chọn nhà cung cấp cấn xóa.");
        }
        
    }//GEN-LAST:event_jButton13ActionPerformed

