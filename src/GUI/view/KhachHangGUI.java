    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        if(!txtCustomerId.getText().equals("")){
            if(customerBLL.getCustomerById(Integer.parseInt(txtCustomerId.getText()))!=null){
                if(customerBLL.deleteCustomer(Integer.parseInt(txtCustomerId.getText()))>0){
                    JOptionPane.showMessageDialog(null, "Xóa thành công.");
                    load();
                    clear();
                }else JOptionPane.showMessageDialog(null, "Xóa thất bại.");
            
        }else{
            JOptionPane.showMessageDialog(null, "Không tìm thấy khách hàng cần xóa.");
        }
        }else{
            JOptionPane.showMessageDialog(null, "Vui lòng chọn khách hàng cấn xóa.");
        }
    }//GEN-LAST:event_jButton9ActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String txtSearch = this.txtSearch.getText();
        if(!txtSearch.equals("")){
            
           ArrayList<customer> listId = customerBLL.searchCustomerById(txtSearch);
           ArrayList<customer> listName = customerBLL.getCustomerByName(txtSearch);
           ArrayList<customer> listPhone = customerBLL.searchCustomerByPhone(txtSearch);
        if(!listId.isEmpty()){
            showTableSearch(listId);
        }
        else if(!listName.isEmpty()){
            showTableSearch(listName);
        }
        else if(!listPhone.isEmpty()){
            showTableSearch(listPhone);
        }
        else JOptionPane.showMessageDialog(null, "Không tìm thấy.");
        }
        else{
            JOptionPane.showMessageDialog(null, "Vui long nhập dữ liệu cần tìm kiếm.");
            load();
        }
    }//GEN-LAST:event_btnSearchActionPerformed


