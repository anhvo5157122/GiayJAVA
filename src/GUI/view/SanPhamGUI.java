    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(!txtId.getText().equals("")){
            if(productBLL.getProductById(Integer.parseInt(txtId.getText()))!=null){
                if(productBLL.deleteProduct(Integer.parseInt(txtId.getText()))>0){
                    JOptionPane.showMessageDialog(null, "Xóa thành công.");
                    load();
                    clear();
                }else JOptionPane.showMessageDialog(null, "Xóa thất bại.");
            
        }else{
            JOptionPane.showMessageDialog(null, "Không tìm thấy sản phẩm cần xóa.");
        }
        }else{
            JOptionPane.showMessageDialog(null, "Vui lòng chọn sản phẩm cấn xóa.");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton29ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton29ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int returnvalue = fileChooser.showOpenDialog(this);
        if(returnvalue==JFileChooser.APPROVE_OPTION){
            File file = fileChooser.getSelectedFile();
            String src = file.getAbsolutePath();
             pathFile = src.replace("\\", "--");
            BufferedImage b;
            try{
                b = ImageIO.read(file);
                jlbImage.setIcon(new ImageIcon(b));
            }catch(Exception e){
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jButton29ActionPerformed
