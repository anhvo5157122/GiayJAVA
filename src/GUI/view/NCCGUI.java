    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        // TODO add your handling code here:
        
        if(!txtSupplierId.getText().equals("")){
            int id = Integer.parseInt(txtSupplierId.getText());
            String supplierName = txtSupplierName.getText();
            String supplierPhone = txtSupplierPhone.getText();
            if(supplierBLL.getSupplierById(id)!=null){
                supplier s = new supplier(id, supplierName, supplierPhone);
                if(supplierBLL.updateSupplier(s)>0){
                    JOptionPane.showMessageDialog(null, "Cập nhật thành công.");
                    load();
                } else{
                    JOptionPane.showMessageDialog(null, "Cập nhật thất bại.");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Không tìm thấy nhà cung cấp cần sửa.");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Vui lòng chọn nhà cung cấp cần sửa.");
        }
        
    }//GEN-LAST:event_jButton12ActionPerformed

