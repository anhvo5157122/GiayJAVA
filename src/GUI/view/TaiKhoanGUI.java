    public void showTable(){
        model.setRowCount(0);
        for(staff s : listStaff){
            model.addRow(new Object[]{
                s.getStaffId(), s.getPassword(), s.getStaffName(), s.getPower(), s.getPhone(), s.getAddress()
            });
        }
    }
