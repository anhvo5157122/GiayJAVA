    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String customerName = txtCustomerName.getText();
        String custmomerPhone = txtCustomerPhone.getText();
        
        customer c = new customer(customerName, custmomerPhone, 0);
        if(!customerName.equals("")&&!custmomerPhone.equals("")){
            if(customerBLL.addCustomer(c)>0){
            JOptionPane.showMessageDialog(null, "Thêm thành công.");
            load();
            clear();
        }
        else{
            JOptionPane.showMessageDialog(null, "Thêm thất bại.");
        }
        }else{
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin nhà cung cấp cần thêm.");
        }
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        if(!txtCustomerId.getText().equals("")){
            int id = Integer.parseInt(txtCustomerId.getText());
            String customerName = txtCustomerName.getText();
            String customerPhone = txtCustomerPhone.getText();
            int customerPoint = Integer.parseInt(txtCustomerPoint.getText());
            if(customerBLL.getCustomerById(id)!=null){
                customer c = new customer(id, customerName, customerPhone,customerPoint);
                if(customerBLL.updateCustomer(c)>0){
                    JOptionPane.showMessageDialog(null, "Cập nhật thành công.");
                    load();
                } else{
                    JOptionPane.showMessageDialog(null, "Cập nhật thất bại.");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Không tìm thấy khách hàng cần sửa.");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Vui lòng chọn khách hàng cần sửa.");
        }
    }//GEN-LAST:event_jButton8ActionPerformed

