    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(!txtName.getText().equals("")&&!txtPrice.getText().equals("")&&pathFile!=null){
            String id = txtId.getText();
            String name = txtName.getText();
            String price = txtPrice.getText();
            String produtSupplier = CBSupplier.getSelectedItem().toString();
            int produtSupplierId=1;
            String color = CBColor.getSelectedItem().toString();
            for(supplier s : supplierBLL.readSupplier()){
            if(s.getSupplierName().equals(produtSupplier)) produtSupplierId = s.getSupplierId();
        }
            if(productBLL.getProductById(Integer.parseInt(id))!=null){
                product p = new product(Integer.parseInt(id), name, Float.parseFloat(price), 0, produtSupplierId, pathFile, color);
                if(productBLL.updateProduct(p)>0){
                    JOptionPane.showMessageDialog(null, "Cập nhật thành công.");
                    load();
                    clear();
                } else{
                    JOptionPane.showMessageDialog(null, "Cập nhật thất bại.");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Không tìm thấy nhân viên cần sửa.");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Vui lòng chọn nhân viên cần sửa.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

