    private void jTable4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable4MouseClicked
        // TODO add your handling code here:
        int row = jTable4.getSelectedRow();
        if(row >=0){
            txtId.setText(jTable4.getValueAt(row, 0).toString());
            
            txtName.setText(jTable4.getValueAt(row, 1).toString());
            String supplierId =jTable4.getValueAt(row, 2).toString();
            for(supplier s : supplierBLL.readSupplier()){
                if(s.getSupplierId()==Integer.parseInt(supplierId)) CBSupplier.setSelectedItem(s.getSupplierName());
            }
            txtPrice.setText(jTable4.getValueAt(row, 4).toString());
            String color = jTable4.getValueAt(row, 5).toString();
            CBColor.setSelectedItem(color);
            pathFile = jTable4.getValueAt(row, 6).toString().replace("--", "\\");
            ImageIcon icon = new ImageIcon(pathFile);
            jlbImage.setIcon(icon);
        }
    }//GEN-LAST:event_jTable4MouseClicked

